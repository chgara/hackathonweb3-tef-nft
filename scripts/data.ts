function mixNFTs(
	arr1: string[],
	arr2: string[],
	arr3: string[]
): string[] {
	const result: string[] = [];
	const length = arr1.length;
	const max = length * 3;
	for (let i = 0; i < max; i++) {
		if (i % 3 === 0) {
			// @ts-ignore
			result.push(arr1.shift());
		} else if (i % 3 === 1) {
			// @ts-ignore
			result.push(arr2.shift());
		} else {
			// @ts-ignore
			result.push(arr3.shift());
		}
	}
	return result;
}

